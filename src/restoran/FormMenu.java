    private void buttonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonInsertActionPerformed

        try
        {
         Statement stat = konek.createStatement(); //membuat variabel untuk statement
                //mengeksekusi query sql
                ResultSet data = stat.executeQuery("SELECT * FROM menu WHERE id_menu='" + idmenu.getText() + "'");
                if (data.next())
                    {
                    //menampilkan pesan bahwa data id menu sudah ada;
                    JOptionPane.showMessageDialog (null, "ID Menu sudah ada!!!", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
                    idmenu.requestFocus(); //menempatkan kursor di idmenu[jTextField]
                } 
                else if (idmenu.getText().equals("") || namamenu.getText().equals("") ||
                        jenismenu.getSelectedItem().equals("") || harga.getText().equals("")) //jika isian jTextField kosong
                {
                    //menampilkan pesan bahwa isian semua jTextField tIdak boleh kosong
                    JOptionPane.showMessageDialog (null, "Data Tidak Boleh Kosong!!!", "PERHATIAN", JOptionPane.WARNING_MESSAGE);
                }
                else
                {
                //query sql untuk insert data
                    String sql = "INSERT INTO menu VALUES ('" + idmenu.getText() + "'"
                            + ",'" + namamenu.getText() + "'"
                            + ",'" + jenismenu.getSelectedItem() + "'"
                            + ",'" + harga.getText() + "')";
                    stat.executeUpdate(sql); //mengeksekusi query sql
                    //menghapusdatainputandiform
                    idmenu.setText("");
                    namamenu.setText("");
                    jenismenu.setSelectedItem("");
                    harga.setText("");
                    idmenu.requestFocus(); //menempatkan kursor di idmenu[jTextField]
                    //menampilkan pesan bahwa data berhasil disimpan
                    JOptionPane.showMessageDialog (null, "Data Berhasil DiSimpan!!!", "SUKSES", JOptionPane.INFORMATION_MESSAGE);
                    tampilData(); //memanggil method tampil data
                }
                stat.close();//menutup statement
            }
        catch (Exception exc)
        {
            System.err.println("Terjadi Kesalahan :" + exc);
        }
    }//GEN-LAST:event_buttonInsertActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
         int ok = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data ini?", "Konfirmasi Dialog", JOptionPane.YES_NO_OPTION);
        if (ok == 0)
        {
            //query sql untuk hapus data
            String sql ="DELETE FROM menu WHERE id_menu='" + idmenu.getText().trim() + "'";
            try
            {
                Statement stat = konek.createStatement();
                stat.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus!", "Ubah Data", JOptionPane.INFORMATION_MESSAGE);
                stat.close();
                
                
                idmenu.setText("");
                namamenu.setText("");
                jenismenu.setSelectedItem("");
                harga.setText("");
                idmenu.enable(true);
                idmenu.requestFocus();
                tampilData();
            }
            catch (Exception exc)
            {
               System.err.println(sql);
               System.err.println("Error ; " + exc);
            }
        }
    
    }//GEN-LAST:event_jButton3ActionPerformed

    private void buttonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUpdateActionPerformed
        
         int ok = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin mengubah data ini?", "Konfirmasi Dialog", JOptionPane.YES_NO_OPTION);
        if (ok == 0)
        {
            try
            {
                //query sql untuk update data
                String sql ="UPDATE menu SET nama_menu = '"+namamenu.getText()
                        +"', jenis_menu = '"+jenismenu.getSelectedItem()
                        +"', harga = '"+harga.getText()
                        +"' WHERE id_menu='" + idmenu.getText().trim() + "'";
                
                try (Statement stat = konek.createStatement()) {
                    stat.executeUpdate(sql);
                    JOptionPane.showMessageDialog(null, "Data berhasil diubah!", "Ubah Data", JOptionPane.INFORMATION_MESSAGE);
                }
                
                idmenu.setText("");
                namamenu.setText("");
                jenismenu.setSelectedItem("");
                harga.setText("");
                idmenu.enable(true);
                idmenu.requestFocus();
                tampilData();
            }
            catch (HeadlessException | SQLException e)
            {
                JOptionPane.showMessageDialog(null, "Perubahan data Gagal"+e.getMessage());
            }
        }
    }//GEN-LAST:event_buttonUpdateActionPerformed

