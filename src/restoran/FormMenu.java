    public void tampilData(){
        DefaultTableModel menu = new DefaultTableModel();
        menu.addColumn("ID Menu");
        menu.addColumn("Nama Menu");
        menu.addColumn("Jenis Menu");
        menu.addColumn("Harga Menu");
        //atur jTable dengan nama kolom di atas
        tabel.setModel(menu);
        
        //menampilkan data dari database kedalam tabel
        try {
            Statement stat = konek.createStatement (); //membuat variabel untuk statement
            ResultSet data = stat.executeQuery("SELECT * FROM menu"); //mengeksekusi query
            while (data.next())  {
                //menambahkan baris dimodel sesuai dengan data dari tabel di database
                menu.addRow (new Object[]   {
                    data.getString("id_menu"),
                    data.getString("nama_menu"),
                    data.getString("jenis_menu"),
                    data.getString("harga"),
                });
                //atur jTable dengan data dari model
                tabel.setModel(menu);
                }
        } catch (SQLException e) {
            System.err.println("Terjadi Kesalahan :" + e);
        }
        
    }
